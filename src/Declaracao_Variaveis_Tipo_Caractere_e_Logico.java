public class Declaracao_Variaveis_Tipo_Caractere_e_Logico {
    public static void main(String[] args) {

        char genero_Funcionario = 'F'; //Declaração de variável tipo char (Caractere).
        boolean aptidao = true; //Declaração de variável tipo boolean (Lógico).

        System.out.printf("\nGênero do funcionário: %c\nApto = %b\n",
                genero_Funcionario, aptidao);
    }
}
